      subroutine federin(ham,cham,n,mattyp)
      implicit integer (a-z)
      real*8 ham, val
      complex*16 cham
      character*80 title
      character*24 mattyp
      dimension ham(n,*), cham(n,*)
      common/io/inp, iout
      if(mattyp.eq.'complex'.or.mattyp.eq.'real-unsymmetric') then
         call czero(cham,n*n)
 10      read(inp,end=20) i, j, val
         cham(i,j)=val
         go to 10
 20      continue
         title='input matrix'
         call prntcm(title,cham,n,n,n,n,iout) 
      else                     
         call rzero(ham,n*n)
 30      read(inp,*,end=40) i, j, ham(i,j)
         go to 30
 40      continue
         title='input matrix'
         call prntrm(title,ham,n,n,n,n,iout) 
      endif 
      return
 1    format(/,5x,'matrix generated by dave feder')      
      end       






