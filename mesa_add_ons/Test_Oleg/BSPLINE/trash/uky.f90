!======================================================================
      Real(8) FUNCTION UKy (i1,j1,i2,j2,k)
!======================================================================
!
!                 k                          
!     Evaluates  U (i1, j1; i2, j2) = 
!
!              k                     k
!             T (i1, j1; i2, j2) +  T (i2, j1; i1, j2) 
!
!----------------------------------------------------------------------


    IMPLICIT NONE
    
    Integer(4), Intent(in) :: i1,j1,i2,j2,k
    
    Real(8), External :: TKy
       
    Uky =  TKy(i1,j1,i2,j2,k) + TKy(i2,j1,i1,j2,k)
    
    END FUNCTION UKy
